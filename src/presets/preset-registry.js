export const PRESET_SETTING_KEYS = Object.freeze([...new Set(
    Object.values(NEMOLORE_PRESETS).flatMap(preset => Object.keys(preset.settings)),
)]);

export function getPreset(id = DEFAULT_PRESET_ID) {
    const preset = NEMOLORE_PRESETS[id];
    if (!preset) throw new TypeError(`Unknown NemoLore preset: ${id}`);
    return preset;
}

export function listPresets() {
    return Object.freeze(Object.values(NEMOLORE_PRESETS));
}

export function resolvePreset(id = DEFAULT_PRESET_ID, overrides = {}) {
    const preset = getPreset(id);
    const knownKeys = new Set(PRESET_SETTING_KEYS);
    const normalizedOverrides = Object.fromEntries(Object.entries(overrides ?? {}).filter(([key]) => knownKeys.has(key)));
    return Object.freeze({
        id: preset.id,
        name: preset.name,
        description: preset.description,
        recommended: preset.recommended === true,
        features: preset.features,
        customized: Object.keys(normalizedOverrides).length > 0,
        overrides: Object.freeze(normalizedOverrides),
        settings: Object.freeze({ ...preset.settings, ...normalizedOverrides }),
        policyVersion: PRESET_POLICY_VERSION,
    });
}
